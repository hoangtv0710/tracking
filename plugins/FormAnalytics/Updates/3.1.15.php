namespace Piwik\Plugins\FormAnalytics;

use Piwik\Updater;
use Piwik\Updates as PiwikUpdates;
use Piwik\Updater\Migration;
use Piwik\Updater\Migration\Factory as MigrationFactory;

/**
 * Update for version 3.1.15.
 */
class Updates_3_1_15 extends PiwikUpdates
{
    /**
     * @var MigrationFactory
     */
    private $migration;

    public function __construct(MigrationFactory $factory)
    {
        $this->migration = $factory;
    }

    public function getMigrations(Updater $updater)
    {
        $migration1 = $this->migration->db->dropIndex('log_form_field', 'unique_field_formview');
        $migration2 = $this->migration->db->addPrimaryKey('log_form_field', array('idlogform', 'field_name', 'idformview'));

        return array(
            $migration2,
            $migration1
        );
    }

    public function doUpdate(Updater $updater)
    {
        $updater->executeMigrations(__FILE__, $this->getMigrations($updater));
    }
}
